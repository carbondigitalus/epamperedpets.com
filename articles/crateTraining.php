    <p><strong class="articleHeader">Crate Training</strong><span class="style2">Mike  Romberger, CBCC-KA<br />
  &copy;2013 <a href="https://www.WhosDoggie.com" target="_blank">Who's Your Doggie</a>.</span></p>
    <p><br />
      <a href="https://www.WhosDoggie.com" target="_blank"><img width="200" height="120" src="../images/whosyourdoggie.png" align="right" hspace="12" alt="Who's Your Doggie14788-Finallogo-D4logo1.jpg" /></a><span class="style2">A crate  serves several purposes in training a dog. All dogs need a place of their own.  This place should allow the dog to feel safe and act as a place to get &ldquo;away  from it all.&rdquo; A crate can be just that place.</span></p>
    <p class="style2">Many dogs  will naturally enjoy their crate and use it as a sleeping and resting area.  Some dogs, however, may have negative feelings about their crate. It is up to  you to make the crate a warm and inviting place. Getting a dog used to a crate  can be a time-consuming activity, but it is time well spent in the end. Here  are some steps to getting your dog acclimated to a crate. Whenever possible,  you want to make the crate experience as positive as you can. Nevertheless,  there are times that you may need to crate the dog before the dog is entirely  comfortable in the crate.</p>
    <p class="style2">Always start  with the crate door open. Throw some treats into the crate to entice the dog to  go in. Start by putting treats near the door and slowly work the treats to the rear  of the crate. In between training sessions, hide some treats in the crate so if  the dog goes in the crate, the dog will find a surprise. As the crate becomes a  positive experience, your dog is more likely to go in the crate in the future. At  this point in the training, you do not want to close the door on the dog. The  idea is to get the dog comfortable in the crate. You may also feed meals to the  dog inside the crate and keep some safe toys in there.</p>
    <p class="style2">When you are  confident that your dog is comfortable in the crate, begin using the cue  &ldquo;crate&rdquo; with the dog. You will simply say &ldquo;crate&rdquo; and throw a treat in the  crate. After the dog understands the meaning of the word &ldquo;crate,&rdquo; you can begin  working on closing the door. </p>
    <p class="style2">Before you  start, you may want to put a toy in the crate to keep the dog occupied. Start  by saying &ldquo;Crate&rdquo; and wait for the dog to enter crate. Gently close the door  and wait one second. Open the door and give the dog a treat. Keep practicing  this and slowly increase the time you close the door until you reach 8 to 10  minutes. When you are practicing, just go about your business and ignore the  dog. </p>
    <p class="style2">Next, start  this process over. This time move out of dog&rsquo;s sight after the door is closed.  Again, start at one second and slowly work towards eight to ten minutes. </p>
    <p class="style2">This process  may take an afternoon or it may take a week or more. Every dog will be  different. The key is to increase the amount of time in the crate gradually. If  your dog begins barking or whining in the crate, you must ignore the dog. Any  attention, positive or negative, the dog receives will reinforce the barking or  whining. This may also be an indication that you increased the dog&rsquo;s time in  the crate too quickly. If this happens, take a break and try again later with  less time for the dog in the crate. At no point should you ever release the dog  from the crate when it is barking or whining. This teaches a behavior you do  not want. To the dog, it means, &ldquo;If I bark, I get out of the crate. Next time I  am in the crate I&rsquo;ll bark and I&rsquo;ll be released.&rdquo; For the dog, this is a great  example of positive reinforcement – the dog performed a behavior (barking) and  was reinforced (released from crate) for that behavior.</p>
    <p class="style2">The final  step of crate training is to continue increasing the amount of time you are  away from the crate. After the dog is comfortable in the crate for about ten  minutes, you increase the time in the crate 20-30% each time. Keep practicing  until you reach the desired time.</p>
    <p><span class="style2">The procedure listed above is the way you want  to introduce to a crate. In reality, many obstacles may prevent you from crate  training in this manner. Work and sleeping at night are just two examples. As  was previously stated, it is up to you to make the crate as positive of an  experience as possible.</span>
