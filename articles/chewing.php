    <p><strong class="articleHeader">Destructive Chewing</strong><span class="style2">Mike  Romberger, CBCC-KA<br />
  &copy;2013 <a href="https://www.WhosDoggie.com" target="_blank">Who's Your Doggie</a>.</span></p>
    <p><br />
      <a href="https://www.WhosDoggie.com" target="_blank"><img width="200" height="120" src="../images/whosyourdoggie.png" align="right" hspace="12" alt="Who's Your Doggie14788-Finallogo-D4logo1.jpg" /></a><span class="style2">I received a  call from a frustrated dog owner. For the caller, the request was simple –  &ldquo;Stop my dog from chewing.&rdquo; When I got to the home, I met the owner and the  dog. I learned that the dog had chewed rugs, pillows, and the wooden legs to  various pieces of furniture. I asked to see the dog&rsquo;s toys. Presented to me was  a disemboweled stuffed animal and sock with a tennis ball tied inside. It did  not take long to see that there was a noticeable gap between the owner&rsquo;s needs  and the dog&rsquo;s needs. </span></p>
    <p class="style2">Chewing is a  natural and healthy part of any dog&rsquo;s life. You may notice that each dog has  its own chewing style. Some dogs will not harm a plush toy while others will  rip through a durable Kong toy. We should always encourage our dogs to chew  since it is both mentally and physically stimulating. Our issue occurs when  dogs chew on things that we, as humans, find unacceptable. For example, the dog  chews on a pair of shoes that you were just about to wear rather than the chew  toy purchased at a pet store. In this case, the chewing on shoes is destructive  chewing.</p>
    <p class="style2">Dogs chew for  a wide variety of reasons. Dogs enjoy chewing and many use it as a relaxing  activity and to reduce stress. Most owners report that destructive chewing  happens when they are not home. For a dog, separation from his family is a  stressful situation. Chewing is an excellent way to reduce that stress and  boredom. Additionally, chewing promotes jaw health and can assist in cleaning the  teeth. It should be noted that chew toys, even those designed for dental  health, are not a substitute for brushing your dog&rsquo;s teeth.</p>
    <p class="style2">Many owners  will discover that most destructive chewing takes place before the age of ten  months. In addition to the reasons above, puppies use their mouths to explore  the world. Anyone that has spent time with a puppy knows that everything a  puppy encounters becomes a chew toy. Our job is to teach our dog which items  are appropriate for chewing. This part is not only easy but can be a great deal  of fun.</p>
    <p class="style2">Present a  single toy to your dog and spend several minutes playing with your dog. This  routine will make it clear to your dog that this is a toy and is all right to  chew. Make this a habit for any new toy. It is also wise to spend a couple  minutes a day playing with your dogs. Many owners tell me that they introduce a  new toy by tossing it on the floor for the dog to find. The biggest drawback to  this practice is that your dog will see anything thrown on the floor as a chew  toy. This may include children&rsquo;s toys, pillows, or clothing. Teaching your dog  what is appropriate to chew and making it fun is more effective than trying to  teach your dog which items are not chew toys.</p>
    <p class="style2">There will be  times that your dog may chew on non-toy items. If this happens, present a high-value  treat or toy to your dog. Entice your dog to drop the item, then say &ldquo;Good!&rdquo;  and give them the toy or treat. You should always use substitutions when taking  a non-toy item away from your dog. This method will teach your dog that a  reward is involved when giving an item to you. Moreover, it shows your dog  which item is appropriate. Otherwise, it is possible your dog consider the item  a treasure that you are keeping for yourself. The dog may try to steal that  item in the future. If this happens repeatedly, your dog may keep the item from  you and start a game of chase. It will not be long before your dog steals the  item just to engage you in play. In rare cases, if you do not substitute a toy,  it may lead to resource guarding, which can lead to aggression. </p>
    <p class="style2">As with many  things in life, the less expensive chew toys tend to be of lower quality. When  choosing your dog&rsquo;s chew toys, try to find sturdy toys that hold up to your dog&rsquo;s  chewing habits. For my dogs, products from Kong and Nylabone are the most  durable. These companies have a wide variety of products to keep dogs  interested. No matter what toys you use to keep your dogs occupied, you always  want to avoid using old clothing. While a pair of shoes you are ready to throw  may seem like the perfect chew toy, it will only lead to confusion for your  dog. Your dog does not have the capability to differentiate your old shoe from  the pair you just bought. When it comes to clothing, the best plan is to keep  your clothes in closets or drawers and off the floor. </p>
    <p class="style2">Sometimes  teaching proper chewing habits is as much about safety and as it is about  cohabitating peacefully. Dogs may chew simply because they are bored. A bored  dog may seek out odd and potentially dangerous item to relieve that boredom. Some  of these items cannot be digested by your dog and may cause blockages. Other  items, such as electrical cords, can be potential deadly. To keep your dog  safe, you may need to manage situation as you train your dog. Baby gates or dog  crates are tools used to keep your dog safe when unsupervised. When using any  management technique, keep appropriate and durable chew toys with your dog. For  more information on crates, see <em>Crate  Training Your Best Friend</em>.</p>
    <p class="style2">A common  complaint I hear about chew toys is that the dog becomes bored with chew toys.  In fact, there is research that supports this. An adult dog may start to lose  interest in a toy in as little as two days. If your dog has as many toys as my  dogs own, you probably have some to spare. Take all your dog&rsquo;s toys and create  two piles. Give one pile to your dog and put away the rest. After 5-7 days,  start introducing toys from the other pile. You will be amazed how happy your  dog will be to see the &ldquo;new&rdquo; toy. Also, if your dog shows little interest in a  toy, you can spice it up with a little beef bouillon or peanut butter. Some  ingenuity on your part can go a long way in teaching proper chewing habits.</p>
