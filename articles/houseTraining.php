    <p><strong class="articleHeader">Low Stress Housetraining</strong><span class="style2">Mike  Romberger, CBCC-KA<br />
  &copy;2013 <a href="https://www.WhosDoggie.com" target="_blank">Who's Your Doggie</a>.</span></p>
    <p><br />
      <a href="https://www.WhosDoggie.com" target="_blank"><img width="200" height="120" src="../images/whosyourdoggie.png" align="right" hspace="12" alt="Who's Your Doggie14788-Finallogo-D4logo1.jpg" /></a><span class="style2">One of the  best parts of my job is helping homeless dogs find a family. It is a wonderful  experience to see a dog and potential new family meet for the first time. At  some point during the encounter, I get THE question, &ldquo;Is he housebroken?&rdquo; This  is a tough, but important, question to answer. First, I recognize that  housesoiling is one of the top reasons for pet relinquishment. Second, this is  too complex a question to answer with a simple &ldquo;yes&rdquo; or &ldquo;no.&rdquo;</span></p>
    <p class="style2">Housetraining  has nothing to do with teaching your dog to avoid using an area under a roof as  a bathroom. It may eventually happen, but that is not how dogs see it. Dogs are  naturally clean animals. They do not want to sleep, eat, or generally hang out  in areas where they urinate or defecate. It is all about teaching a dog the  most appropriate places to go potty. Even if an adult dog you brought home was  housetrained in his foster home, you will still need to show him the new potty  areas. You will find this document useful if you are bringing home a puppy, an  adult dog, or have a dog that has lapses in housetraining.</p>
    <p class="style2">One of the  tools I always recommend for is a dog crate. For more information on crates,  see my companion article, &ldquo;Crate Training  Your Best Friend.&rdquo; Housetraining and crate training go hand-in-hand. The  crate uses your dog&rsquo;s natural cleanliness to support housetraining. A properly  sized crate should be about the same length as your dog with just enough room  to turn around and lie down. A crate is too large if your dog can go potty in  one corner and comfortably lie elsewhere in the crate. </p>
    <p class="style2">Once you have  the crate in place, you can begin housetraining. The three keys to Low Stress  Housetraining are routine, routine, and routine. By having a consistent plan  paired with patience, your dog will soon learn that outside is the only place  to go potty.</p>
    <p class="style2">Start your  routine by picking the &ldquo;potty area.&rdquo; It should be a convenient spot and  preferably has some lighting at night. Take your dog outside to the potty area  on an hourly basis. Stop there and do not move around. Simply say &ldquo;go potty&rdquo; to  the dog and wait. Since you do not want potty time to be confused with  playtime, ignore your dog&rsquo;s attempts to engage you in play. If your dog urinates  or defecates, say &ldquo;Good&rdquo; and give the dog a treat. After the dog finishes, you  can then play with or walk the dog as a reward. This simple routine reinforces  going potty outside. The more frequently this is done, the easier it will be  for the dog to want to go outside for a potty break.</p>
    <p class="style2">If your dog  does not go potty within 5-10 minutes, take the dog back inside. Keep a close  eye on the dog or put the dog in the crate. At this point, it is crucial that  the dog does not have an accident. Any time your dog has an opportunity to have  an accident inside the home; it allows your dog believe that there are multiple  potty areas. After about 15 minutes, take the dog outside and try again.</p>
    <p class="style2">It will not  take long for you and your dog to develop a routine. There are certain times  when your dog should have a potty break.</p>
    <ul>
      <li class="listItems">When the dog wakes in the morning</li>
      <li class="listItems">Five  to twenty minutes after eating a meal</li>
      <li class="listItems">Before the dog is placed in the       crate</li>
      <li class="listItems">Any time the dog will be unable       to go outside for several hours</li>
      <li class="listItems">Prior to bedtime</li>
    </ul>
    <p class="style2">There are a  couple exceptions for puppies. A puppy cannot be fully housetrained until  around six months of age. This is when the puppy develops the proper  musculature to be housetrained. While a crate is a great tool, it should never be  overused. A puppy left in the crate too long is forced to go potty inside the  crate. This can make housetraining more difficult since the puppy is encouraged  to lie in his own filth. As a general rule of thumb, a puppy can remain in a  crate one hour for every month in age plus one. A two-month-old puppy can  remain in the crate for three hours, or a four month old for five hours. So set  your alarm to wake yourself at night. You may need to hire a dog walker if you  are unable to get away from work during the day.</p>
    <p class="style2">Your feeding  schedule can assist in your housetraining efforts. It is wise to time feed  rather than free feed your dog. Free feeding is allowing your dog access to  food throughout the day. A timed feeding schedule allows you to create a  routine. With this schedule, you will feed your dog 2-3 times a day for 10-15  minutes. Save any leftover food for the next meal. In short, if you know when  the food is going in, you will know when it needs to come out!</p>
    <p class="style2">As was  mentioned, you want to go to great lengths to avoid accidents inside. However,  it may still happen. If an accident occurs, DO NOT punish the dog. The only  thing it will teach your dog is to avoid having an accident in front of you. If  your dog has an accident inside or in the crate, take the dog outside for a  potty break. Then clean the mess thoroughly with an enzymatic cleaner to  destroy the odor. While carpet and floor cleaners are great at removing stains,  odors stay behind. Use this accident as a learning experience. Did your dog  give any signals that it was time to go potty? Did you wait too long to take your  dog outside? Was the stool soft or runny, indicating your dog may be sick? </p>
    <p class="style2">Low Stress  Housetraining is all about understanding your dog&rsquo;s needs and using his natural  abilities to your advantage. Before long, you will begin noticing your dog&rsquo;s  signals to go potty. After you have a routine established and can read the  dog&rsquo;s signals, you will be well on your way to having a housetrained dog.</p>
