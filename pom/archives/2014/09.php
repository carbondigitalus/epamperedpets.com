            <p>Clyde and his sister Bonnie came from the shelter to live with me when they were a year and a half old.  They had very different personalities:  Bonnie was vivacious and curious and vocal, while Clyde was aloof and silent.  We had more than eight years of fun and companionship together before Bonnie was diagnosed with a serious heart problem.  She died last fall, and Clyde and I miss her very much.</p>
            <p>I was surprised at the change in Clyde's behavior after Bonnie left us; he actually began to take over some of her &quot;jobs,&quot; like meowing out the kitchen window when I come home, supervising meals, and showing affection.</p>
            <p>The most remarkable thing has been Clyde's transformation into a caregiver for me.  When I was recovering from cancer surgery, I slept with a pillow beside me to protect the incision.  Clyde would gently climb onto the pillow and sleep there all night, as if to protect me and keep the pillow from moving around.  Often during his nap times he would appear beside me, just to check up, and then go back to sleep.  When I was very unsteady from chemo, he would never rub up against me or wind around my ankles when I walked, as if he knew my balance was off.  My favorite is the &quot;curfew,&quot; which is still in force:  every night, sometime between 10 and 11, he comes to find me and sits and stares at me (you know how unnerving that can be!) until I get up to go to bed.</p>
            <p>Then he accompanies me every step of the way and supervises, all the while talking a blue streak!  He keeps up this &quot;encouragement&quot; until I actually get into bed. Just as Clyde is taking care of me, Pampered Pets has taken care of my pets for more than eighteen years.  I don't know what we would have done without them!</p>
